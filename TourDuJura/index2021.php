<p class="titleCenter">Résumé !!! </p>
<fieldset>
<p>
Le départ sera donné le samedi matin à 5H00 à la salle polyvalente des Breuleux en direction des Bois 
et commencer à fouler les sentiers francs-montagnards  </br> </br>
Ensuite l'équipe va longer le Doubs depuis biaufonds avant de remonter la ville de la chaux de fonds puis prendre la direction de la vue des alpes  </br></br>
De là, il faudra se diriger sur Chasseral en passant par la combe Biosse  et suivre la magnifique crête pour arriver sur les gorges du Taubenloch afin d’attaquer la Bözingenberg en passant par le Zoo de Bienne
</br></br>

Depuis le village de Romont notre périple Suisse Allemand va commencer avec la Grencheberg  jusqu'à Gansbrunnen en passant par le Weissensteinavant un retour dans notre beau canton par le village de crémine direction la capitale. </br></br>

Depuis celle-ci, le parcours va amener les coureurs sur l'antenne des ordons qui va nous conduire en Ajoie.</br></br>
La dernière ligne droite commence à Porrentruy pour rejoindre la cité médiévale de Saint-Ursanne pour ensuite remonter sur les Franches-Montagnes par la Combe Tabeillon.</br></br>

A l'étang de la Gruère, plus que quelques foulées avant d'arriver aux Breuleux où l'arrivée final récompensera les coureurs.</p>

